-- 产品包装单位模板（平台级，开通商家时随产品一起复制）
-- 常见耗材的包装换算：箱/件/包 → 基本单位（卷/公斤/个等）

CREATE TABLE TAB_GOODS_UNIT_TEMPLATE (
    S_ID            VARCHAR(50)   NOT NULL DEFAULT '' COMMENT '主键',
    S_GOODS_CODE    VARCHAR(50)   NOT NULL DEFAULT '' COMMENT '关联产品编码（对应模板产品 S_CODE）',
    S_UNIT_NAME     VARCHAR(50)   NOT NULL DEFAULT '' COMMENT '包装单位名称（如 箱、件、包）',
    I_CONVERT_RATE  INT           NOT NULL DEFAULT 1 COMMENT '换算率（1包装单位=N基本单位）',
    PRIMARY KEY (S_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='耗材平台-产品包装单位模板（平台级）';

